package command;

public class MenuChoiceCommand {
	
	String menuId;
	String choiceTitle;
	String choiceType;
	String choiceOption;
	String choicePrice;
	String menuChoiceNum;
	
	
	public String getMenuChoiceNum() {
		return menuChoiceNum;
	}
	public void setMenuChoiceNum(String menuChoiceNum) {
		this.menuChoiceNum = menuChoiceNum;
	}
	public String getChoiceOption() {
		return choiceOption;
	}
	public void setChoiceOption(String choiceOption) {
		this.choiceOption = choiceOption;
	}
	public String getChoicePrice() {
		return choicePrice;
	}
	public void setChoicePrice(String choicePrice) {
		this.choicePrice = choicePrice;
	}
	public String getMenuId() {
		return menuId;
	}
	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}
	public String getChoiceTitle() {
		return choiceTitle;
	}
	public void setChoiceTitle(String choiceTitle) {
		this.choiceTitle = choiceTitle;
	}
	public String getChoiceType() {
		return choiceType;
	}
	public void setChoiceType(String choiceType) {
		this.choiceType = choiceType;
	}
	
	
	
}
