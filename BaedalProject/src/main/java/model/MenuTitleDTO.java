package model;

public class MenuTitleDTO {
	String menuTitleNum;
	String menuTitleName;
	String comId;
	
	public String getMenuTitleNum() {
		return menuTitleNum;
	}
	public void setMenuTitleNum(String menuTitleNum) {
		this.menuTitleNum = menuTitleNum;
	}
	public String getMenuTitleName() {
		return menuTitleName;
	}
	public void setMenuTitleName(String menuTitleName) {
		this.menuTitleName = menuTitleName;
	}
	public String getComId() {
		return comId;
	}
	public void setComId(String comId) {
		this.comId = comId;
	}
	
	
}
